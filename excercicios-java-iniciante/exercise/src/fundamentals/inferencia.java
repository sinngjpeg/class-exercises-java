package fundamentals;

public class inferencia {

	public static void main(String[] args) {

		double a = 4.5;
		System.out.println(a);

		var b = 4.5;
		System.out.println(b);

		var c = "Outro texto";
		System.out.println(c);

		// c = 4.5;

		double d; // variavel foi declarada
		d = 123.65;
		System.out.println(d); // usada!

	}

}
