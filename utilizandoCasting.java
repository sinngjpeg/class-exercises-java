
public class utilizandoCasting {

	public static void main(String[] args) {

		int a, b;
		double resultado;

		a = 5;
		b = 2;

		// casting conversao explicita dos valores/dados
		resultado = (double) a / b;

		System.out.println(resultado);

		double c;
		int d;

		c = 5.0;
		d = (int) c;

		System.out.println(d);

	}

}
